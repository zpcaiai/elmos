    [Fact]
    public void EngineJobsAreTenantScopedAndIdempotent()
    {
        Write("App.csproj", "<Project Sdk=\"Microsoft.NET.Sdk\"><PropertyGroup><TargetFramework>net10.0</TargetFramework></PropertyGroup></Project>");
        Write("A.cs", "public class A { }");
        var engine = new DotnetEngine(root);
        var first = engine.Scan(new("org-a", "snapshot", ".", "STATIC", "c1", "same"));
        var repeated = engine.Scan(new("org-a", "snapshot", ".", "STATIC", "c2", "same"));
        var otherTenant = engine.Scan(new("org-b", "snapshot", ".", "STATIC", "c3", "same"));
        var conflictingReplay = engine.Scan(new("org-a", "different-snapshot", ".", "STATIC", "c4", "same"));
        Assert.Equal(first.JobId, repeated.JobId);
        Assert.NotEqual(first.JobId, otherTenant.JobId);
        Assert.Equal(JobStatus.Failed, engine.GetJob("org-b", first.JobId).Status);
        Assert.Equal(DotnetErrorCode.PolicyBlocked, conflictingReplay.Error?.ErrorCode);
        Assert.Contains("different immutable inputs", conflictingReplay.Error?.Message);
        Assert.Equal(DotnetErrorCode.PolicyBlocked, engine.Cancel("org-a", first.JobId).Error?.ErrorCode);
    }

    [Fact]
    public void ExecuteIdempotencyFingerprintIncludesBudgetWorkspaceAndPolicy()
    {
        var engine = new DotnetEngine(root);
        var step = new StepDefinition("project", ExecutorType.ProjectSystemTransformation,
            new Dictionary<string, object?> { ["projectXml"] = LegacyProject("v4.8", "") });
        var request = new ExecuteStepRequest("org-a", "run", 1, step, ".", "deadbeef",
            new ExecutionBudget(30, 10, 10_000, 0), new Dictionary<string, object?> { ["mode"] = "safe" },
            "corr", "execute-same");
        Assert.Equal(JobStatus.Succeeded, engine.ExecuteStep(request).Status);
        var conflict = engine.ExecuteStep(request with { ExecutionBudget = new ExecutionBudget(60, 10, 10_000, 0) });
        Assert.Equal(JobStatus.Failed, conflict.Status);
        Assert.Contains("different immutable inputs", conflict.Error?.Message);
    }

    [Fact]
    public void DeprecatedUpgradeAssistantCannotOwnPolicyOrMerge()
    {
        var adapter = Assert.Single(ModernizationToolRegistry.All, item => item.Name == "UPGRADE_ASSISTANT");
        Assert.Equal(ToolAuthority.DeprecatedLegacyAdapter, adapter.Authority);
        Assert.False(adapter.CanSelectTarget);
        Assert.False(adapter.CanAcceptRisk);
        Assert.False(adapter.CanMerge);
    }

    private void Write(string relative, string content)
    {
        var path = Path.Combine(root, relative);
        Directory.CreateDirectory(Path.GetDirectoryName(path)!);
        File.WriteAllText(path, content);
    }

    private static string LegacyProject(string framework, string extra) => $"<Project ToolsVersion=\"15.0\" xmlns=\"http://schemas.microsoft.com/developer/msbuild/2003\"><PropertyGroup><TargetFrameworkVersion>{framework}</TargetFrameworkVersion></PropertyGroup><ItemGroup><Compile Include=\"A.cs\" /></ItemGroup>{extra}<Import Project=\"$(MSBuildToolsPath)\\Microsoft.CSharp.targets\" /></Project>";
}
