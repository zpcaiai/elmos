    [Fact]
    public void CapabilitiesDeclareSecondEngineWithoutControlPlaneResponsibilities()
    {
        var capabilities = CapabilityManifest.Current;
        Assert.Equal("ELMOS_DOTNET", capabilities.Engine);
        Assert.Contains("C_SHARP", capabilities.Languages);
        Assert.Contains("VISUAL_BASIC_INVENTORY", capabilities.Languages);
        Assert.Equal(3, capabilities.RunnerProfiles.Count);
    }

    [Fact]
    public void StaticDiscoveryDoesNotChooseAmongMultipleSolutionsOrHideUnsupportedProjects()
    {
        Write("a.sln", "Microsoft Visual Studio Solution File");
        Write("b.slnx", "<Solution />");
        Write("src/App/App.csproj", LegacyProject("v4.8", "<UsingTask TaskName=\"Unsafe\" AssemblyFile=\"unsafe.dll\" />"));
        Write("src/Vb/Vb.vbproj", "<Project Sdk=\"Microsoft.NET.Sdk\"><PropertyGroup><TargetFramework>net10.0</TargetFramework></PropertyGroup></Project>");
        var inventory = new SafeSolutionDiscovery().Scan(root, "snapshot-1");
        Assert.True(inventory.PrimarySelectionAmbiguous);
        Assert.Contains("MULTIPLE_SOLUTIONS_AMBIGUOUS", inventory.Findings);
        Assert.Contains("CUSTOM_MSBUILD_TASK", inventory.Findings);
        Assert.Contains("src/Vb/Vb.vbproj", inventory.UnsupportedProjects);
    }

    [Fact]
    public void DiscoveryKeepsAllSolutionAndConditionalConfigurationsSeparate()
    {
        Write("App.sln", "Microsoft Visual Studio Solution File\nGlobal\nGlobalSection(SolutionConfigurationPlatforms) = preSolution\nDebug|Any CPU = Debug|Any CPU\nRelease|x64 = Release|x64\nEndGlobalSection\nEndGlobal");
        Write("App.csproj", "<Project Sdk=\"Microsoft.NET.Sdk\"><PropertyGroup><TargetFramework>net10.0</TargetFramework></PropertyGroup><PropertyGroup Condition=\"'$(Configuration)|$(Platform)' == 'Staging|x86'\"><PlatformTarget>x86</PlatformTarget></PropertyGroup></Project>");
        var project = Assert.Single(new SafeSolutionDiscovery().Scan(root, "snapshot-config").Projects);
        Assert.Contains("Staging", project.Configurations);
        Assert.Contains("Release", project.Configurations);
        Assert.DoesNotContain("x64", project.Configurations);
        Assert.Contains("x86", project.Platforms);
        Assert.Contains("x64", project.Platforms);
    }

    [Fact]
    public void FingerprintKeepsWindowsAndDynamicDependenciesVisible()
    {
        Write("App.csproj", LegacyProject("v4.8", ""));
        Write("Service.cs", "using Microsoft.Win32; class S { void X(){ Registry.GetValue(\"a\",\"b\",null); System.Reflection.Assembly.Load(\"plugin\"); } }");
        var inventory = new SafeSolutionDiscovery().Scan(root, "snapshot-2");
        var fingerprint = new TechnologyFingerprinter().Fingerprint(root, inventory);
        Assert.Contains(fingerprint.Findings, finding => finding.Technology == "WINDOWS_REGISTRY" && finding.Portability == Portability.WindowsOnlySupported);
        Assert.True(fingerprint.HasDynamicDependencies);
        Assert.True(fingerprint.Confidence < 0.9m);
    }

    [Fact]
    public async Task RoslynUsesSemanticOperationsAndProtectsGeneratedCode()
    {
        Write("App.csproj", "<Project Sdk=\"Microsoft.NET.Sdk\"><PropertyGroup><TargetFramework>net10.0</TargetFramework></PropertyGroup></Project>");
        Write("A.cs", "namespace Demo; public class A { public void Run(){ B.Call(); } } public static class B { public static void Call(){} }");
        Write("Generated.g.cs", "// <auto-generated />\nnamespace Demo; public class Generated { }");
        var inventory = new SafeSolutionDiscovery().Scan(root, "snapshot-3");
        var graph = await new RoslynGraphBuilder().BuildAsync(root, inventory, CancellationToken.None);
        Assert.Contains(graph.Edges, edge => edge.Kind == "DIRECT_STATIC_CALL");
        Assert.Contains(graph.Symbols, symbol => symbol.GeneratedCode);
        Assert.DoesNotContain(graph.Symbols, symbol => string.IsNullOrWhiteSpace(symbol.SymbolId));
    }

    [Fact]
    public void ProjectSystemMovesToSdkStyleWhileKeepingNet48AndIsIdempotent()
    {
        var modernizer = new SdkProjectModernizer();
        var first = modernizer.Transform(LegacyProject("v4.8", "<Target Name=\"Custom\" />"), "<packages><package id=\"Newtonsoft.Json\" version=\"13.0.3\" /></packages>");
        var second = modernizer.Transform(first.ProjectXml, null);
        Assert.Contains("Sdk=\"Microsoft.NET.Sdk\"", first.ProjectXml);
        Assert.Contains("<TargetFramework>net48</TargetFramework>", first.ProjectXml);
        Assert.Contains("Newtonsoft.Json", first.ProjectXml);
        Assert.Contains("Target:Custom", first.PreservedCustomizations);
        Assert.Equal(first.ProjectXml, second.ProjectXml);
    }

    [Fact]
    public void PlannerDoesNotRecommendLinuxForRegistryAndUsesSideBySideForWebForms()
    {
        var inventory = new SolutionInventory("snapshot", [], false, [new("p", "App.csproj", ProjectLanguageSupport.CSharpFullSupport, ProjectFormat.LegacyNonSdk, ["v4.8"], ["Debug"], ["AnyCPU"], [], [], [], [])], [], []);
        var fingerprint = new TechnologyFingerprint("f", [new("1", "WINDOWS_REGISTRY", Portability.WindowsOnlySupported, 1m, "x", true), new("2", "ASP_NET_WEB_FORMS", Portability.RequiresRedesign, 1m, "x", true)], false, 1m, []);
        var plan = new ConservativeMigrationPlanner().Plan(inventory, fingerprint, new([], [], [], CompilationState.FullCompilation), "STRATEGIC");
        Assert.Equal(TargetStrategy.IncrementalSideBySide, plan.Recommended.Strategy);
        Assert.Equal("WINDOWS", plan.Recommended.OperatingSystem);
        Assert.Contains(plan.Steps, step => step.StepType == "YARP_SYSTEM_WEB_ADAPTER");
    }

